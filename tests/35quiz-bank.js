// 保存自定义题目
function saveCustomQuiz(item) {
    var custom = [];
    try { custom = JSON.parse(localStorage.getItem('ming_quiz_bank') || '[]'); } catch (e) {}
    custom.push(item);
    localStorage.setItem('ming_quiz_bank', JSON.stringify(custom));
}


// ==================== 一、核心铁律 (5条) ====================
/*
1. 错误可见，不靠瞎猜
   - 代码异常必须在页面底部红色面板显示，不能静默卡死 (24error-capture)。
   - 手机端一样能看到报错，完全替代 F12。

2. 禁止凭感觉改代码
   - 凡涉战斗规则、数值、Buff、代码结构，不确定就问。
   - 严禁基于“猜测”的代码修改或“瞎蒙”的旧代码块。

2.5. 定位优先，修复滞后
   - 提出修复方案前，必须先完成根因定位（诊断日志、控制台输出、用户数据）。
   - 同一问题连续两次修复无效，立即停止提交新方案，回到最小可验证单元重新排查。
   - 禁止在“不确定根因”的情况下提供代码修改。

3. 拆解验证优先
   - 同一问题两次修复无效，立即停手，回到最小可验证单元。
   - 所有核心计算函数（calcDamage等）必须能跑单元测试 (25unit-tests)。

4. 函数职责单一，看动分离
   - 纯计算（calcDamage）、纯判定（shouldZhangSwitch）、纯渲染（renderGrid）必须隔离。
   - 本次已成功拆分 processUnitAttack，未来继续。

5. 同类错误不犯两次
   - 本次对话中的典型案例（stats未定义、VER重复声明、旧代码块搜不到）已归档。
   - 执行前调取本文件核对。
*/

